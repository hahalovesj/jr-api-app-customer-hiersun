package com.hiersun.jewelry.api.entity.vo;

public class Review {
	private String reviewExplain;
	private String reviewResult;
	private String applyTime;
	private String reviewTime;
	private String returnTime;
	private String appraisalTime;
	private String createTime;
	public String getReviewExplain() {
		return reviewExplain;
	}
	public void setReviewExplain(String reviewExplain) {
		this.reviewExplain = reviewExplain;
	}
	public String getReviewResult() {
		return reviewResult;
	}
	public void setReviewResult(String reviewResult) {
		this.reviewResult = reviewResult;
	}
	public String getApplyTime() {
		return applyTime;
	}
	public void setApplyTime(String applyTime) {
		this.applyTime = applyTime;
	}
	public String getReviewTime() {
		return reviewTime;
	}
	public void setReviewTime(String reviewTime) {
		this.reviewTime = reviewTime;
	}
	public String getReturnTime() {
		return returnTime;
	}
	public void setReturnTime(String returnTime) {
		this.returnTime = returnTime;
	}
	public String getAppraisalTime() {
		return appraisalTime;
	}
	public void setAppraisalTime(String appraisalTime) {
		this.appraisalTime = appraisalTime;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	
	
}
